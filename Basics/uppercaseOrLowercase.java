public class uppercaseOrLowercase
{
	public static void main(String args[])
	{
		char a = 'h';
		char b = 'G';
		
		if(a>='a' && a<='z')
		System.out.println("a is Lower Case");
		
		else
		System.out.println("a is Upper Case");

		if(b>='a' && b<='z')
		System.out.println("b is Lower Case");
		
		else
		System.out.println("b is Upper Case");
	}
}