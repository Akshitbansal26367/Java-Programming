class percentage
{
	public static void main(String args[])
	{
		double physics = 87, maths = 84, chemistry = 78, physical_education = 93, english = 84;
		double total = physics+maths+chemistry+physical_education+english;
		double average = (physics+maths+chemistry+physical_education+english)/5;
		System.out.println("I scored "+total+" marks out of 500");
		System.out.println("I scored "+average+"% in class 12th");
	}
}